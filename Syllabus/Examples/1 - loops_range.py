#For loop range examples

print("Here we are counting from 0 to 9")
for current_index in range(0, 10):
    print("The current iteration of the for loop: %s" % current_index)

print("Here we are counting from 5 to 9")
for current_index in range(5, 10):
    print("The current iteration of the for loop: %s" % current_index)

print("Now we are counting from 10 down to -1")
for current_index in range(10, 5, -1):
    print("The current iteration of the for loop: %s" % current_index)
